package gastmappers.rpgmapper.mapperutilities.Functions.BuiltIn;

import org.antlr.v4.runtime.tree.ParseTree;
import org.rpgleparser.RpgParser.Bif_dateContext;

import gastmappers.rpgmapper.mapperutilities.Functions.BuiltIn.Factory.BuiltInFunction;
import gastmappers.rpgmapper.mapperutilities.Statements.Factors.Factor;
import gastmappers.rpgmapper.mapperutilities.freecontext.FreeContextAnalyzer;

public class DateFunction extends BuiltInFunction implements Factor {

	private Factor expression, dateFormat;

	public DateFunction(String functionName) {
		super(functionName);
	}

	public Factor getExpression() {
		return expression;
	}

	public void setExpression(Factor expression) {
		this.expression = expression;
	}

	public Factor getDateFormat() {
		return dateFormat;
	}

	public void setDateFormat(Factor dateFormat) {
		this.dateFormat = dateFormat;
	}

	@Override
	public String getValue() {

		return null;
	}

	@Override
	public String getType() {

		return null;
	}

	@Override
	public void setValue(String value) {

	}

	@Override
	public void setType(String type) {

	}

	@Override
	public void generateFactor(ParseTree parseTree) {
		Bif_dateContext dateContext = (Bif_dateContext) parseTree;

		this.dateFormat = FreeContextAnalyzer.getFreeContextFactor(dateContext.dateformat);
		this.expression = this.getSingleExpression(parseTree);
	}

}
